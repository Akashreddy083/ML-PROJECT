plt.ylabel("Final Grade")
plt.title("Study Time vs Performance")
plt.show()

plt.figure()
plt.scatter(df['absences'], df['G3'])
plt.xlabel("Absences")
plt.ylabel("Final Grade")
plt.title("Absences vs Performance")
plt.show()